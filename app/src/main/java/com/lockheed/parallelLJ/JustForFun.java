package com.lockheed.parallelLJ;

public class JustForFun {
    int age;
    String name;

    public JustForFun(String jack, int i) {
        age=i;
        name=jack;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }
}
